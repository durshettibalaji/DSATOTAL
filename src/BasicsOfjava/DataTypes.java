package BasicsOfjava;

public class DataTypes {
//    We are going to list the primitive data types and the non-primitive (reference) data types in Java.
//    Primitive data types are predefined by the language and named by a keyword.
//            Non-primitive data types are created by the programmer and are not defined by Java (except for String).
//
//    Primitive Data Types:
//
//    byte: 8-bit integer. Range: -128 to 127.
//
//    short: 16-bit integer. Range: -32,768 to 32,767.
//
//    int: 32-bit integer. Range: -2^31 to 2^31-1.
//
//    long: 64-bit integer. Range: -2^63 to 2^63-1.
//
//    float: 32-bit floating point. Approximate range: ±3.4e−038 to ±3.4e+038.
//
//    double: 64-bit floating point. Approximate range: ±1.7e−308 to ±1.7e+308.
//
//    char: 16-bit Unicode character. Range: '\u0000' (0) to '\uffff' (65,535).
//
//    boolean: represents one bit of information, but its size isn't precisely defined. Can be true or false.
//
//    Non-Primitive Data Types (Reference Types):
//
//    Classes (e.g., String, Arrays, and any user-defined classes)
//
//    Interfaces
//
//            Arrays
//
//    Example of declaring variables of primitive types:
//    byte myByte = 100;
//    short myShort = 5000;
//    int myInt = 100000;
//    long myLong = 15000000000L; // Note the 'L' suffix for long
//    float myFloat = 5.75f; // Note the 'f' suffix for float
//    double myDouble = 19.99d; // The 'd' is optional for double
//    char myChar = 'A';
//    boolean myBoolean = true;
//
//    Example of non-primitive types:
//    String myString = "Hello";
//    int[] myArray = {1, 2, 3};
//
//    Note: Non-primitive types are objects and can be null. Primitive types cannot be null.

}

